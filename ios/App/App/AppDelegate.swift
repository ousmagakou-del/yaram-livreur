        // Called when the app was launched with an activity, including Universal Links.
        return ApplicationDelegateProxy.shared.application(application, continue: userActivity, restorationHandler: restorationHandler)
    }

    // ═══════════════════════════════════════════════════════════════════
    //  PUSH NOTIFICATIONS — Bridging APNs natif → @capacitor/push-notifications
    // ═══════════════════════════════════════════════════════════════════
    //
    // Ces 2 méthodes sont OBLIGATOIRES pour que le plugin Capacitor récupère
    // le device token APNs. Sans elles, register() ne reçoit jamais le token,
    // donc OneSignal ne reçoit jamais rien, donc aucune notif n'arrive.
    //
    // Doc : https://capacitorjs.com/docs/apis/push-notifications#bridging-with-the-app

    func application(_ application: UIApplication,
                     didRegisterForRemoteNotificationsWithDeviceToken deviceToken: Data) {
        NotificationCenter.default.post(
            name: .capacitorDidRegisterForRemoteNotifications,
            object: deviceToken
        )
    }

    func application(_ application: UIApplication,
                     didFailToRegisterForRemoteNotificationsWithError error: Error) {
        NotificationCenter.default.post(
            name: .capacitorDidFailToRegisterForRemoteNotifications,
            object: error
        )
    }

}
